
public class TrianguloNormal {

	public static void main(String[] args) {
		int star=6;
		for (int i=1; i<=star; i++) {   //inicializador; condicion; incremental;
			for(int star2 = 0; star2<i; star2++) {
				System.out.print("*");//imprimir sin salto de linea
			}//for2
			System.out.println(); //aqui imprime el salto de linea despues de los asteriscos o estrellas

	}//for1

}//main
}//class TrianguloNormal
